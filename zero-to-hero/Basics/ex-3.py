# Create a new Python script that uses the following operators: =, ==, >=, *, **, /, //, %, +=, *=

a, b = 10, 20
print(a == b)       # => False
print(a >= 100)     # => False
print(2 * 2)        # => 4
print(2 ** 128)     # => 340282366920938463463374607431768211456
print(22 / 6)       # => 3.6
print(22 // 6)      # => 3
print(22 % 6)       # => 4
print(21 // 4)      # => 5
print(15 % 2)       # => 1

a += 5
b *= 3
